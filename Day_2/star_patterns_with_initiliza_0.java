/* star_patterns_with_initiliza_0 and condition n-1*/

package Day_2;

public class star_patterns_with_initiliza_0 {
    public static void main(String[] args) {
        int n=4;
        for(int i=0;i<=n-1;i++){
            for(int j=0;j<=n-1;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
